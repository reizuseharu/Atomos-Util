package com.reizu.core.util

/**
 * Generates empty mutable list
 */
fun <T> emptyMutableList(): MutableList<T> = mutableListOf()

/**
 * Generates empty mutable set
 */
fun <T> emptyMutableSet(): MutableSet<T> = mutableSetOf()

/**
 * Generates empty mutable map
 */
fun <T, U> emptyMutableMap(): MutableMap<T, U> = mutableMapOf()
